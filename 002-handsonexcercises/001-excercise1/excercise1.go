package main

import "fmt"

var x int
var y string
var z bool

func main() {
	x = 42
	y = "James Bond"
	z = true
	fmt.Println("The value of x is :", x)
	fmt.Println("The value of y is :", y)
	fmt.Println("The value of z is :", z)
	fmt.Println("The value of x is :", x, "\n The value of y is :", y, "\n The value of z is :", z)
}
